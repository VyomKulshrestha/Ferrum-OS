/// Look up the 16-byte glyph bitmap for an ASCII character.
///
/// Returns a reference to 16 consecutive bytes from `FONT_DATA`, where
/// each byte encodes one scanline row (MSB = leftmost pixel).
///
/// Characters outside 0–127 are clamped to 0 (blank glyph).
pub fn glyph(ch: u8) -> &'static [u8; 16] {
    let index = if ch > 127 { 0 } else { ch as usize };
    // Safety: index is guaranteed to be in 0..128, and each glyph is
    // exactly 16 bytes, so the slice [index*16 .. index*16+16] is always
    // within bounds of the 2048-byte array.
    FONT_DATA[index * 16..][..16].try_into().unwrap()
}
